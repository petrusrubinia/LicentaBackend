package com.backLicenta.aliment.domain;

public class Pereche {
    private double A;
    private double B;

    public Pereche(double a, double b) {
        A = a;
        B = b;
    }

    public double getA() {
        return A;
    }

    public void setA(double a) {
        A = a;
    }

    public double getB() {
        return B;
    }

    public void setB(double b) {
        B = b;
    }
}
